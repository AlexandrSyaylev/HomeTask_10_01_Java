package ru.netology;

public class MovieElement {
    private int id;
    private String name;
    private String genre;
    private String imageUrl;
    private boolean exclusive;

    public MovieElement(int id, String name, String genre, String imageUrl, boolean exclusive) {
        this.id = id;
        this.name = name;
        this.genre = genre;
        this.imageUrl = imageUrl;
        this.exclusive = exclusive;
    }

    ;
}

